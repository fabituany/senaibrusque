                                Florianópolis, 30.11.2015 – A décima segunda edição do EnModa traz como novidade o aprofundamento da parceria entre faculdade e indústria. Realizado pelo curso superior em Tecnologia em Design de Moda do SENAI em Criciúma, em parceria com a Unesc, o evento será realizado na próxima quarta-feira, dia 2, no Siso’s Hall. Nesta edição, que traz as macrotendências para o inverno 2017, os modelos que serão exibidos na passarela foram concebidos em conjunto com 23 empresas ligadas ao setor do vestuário. A partir dos projetos realizados pelos acadêmicos da sexta fase do curso, cada empresa escolheu três looks para apoiar a criação, sendo dois com conceito comercial e um conceitual.
                            </p>
                            <p>
                                Ao longo do segundo semestre de 2015 o EmModa evoluiu até chegar aos modelos confeccionados pelos próprios alunos, nos laboratórios do Senai. “O projeto trabalha a criação dentro de uma realidade de mercado, permitindo a maior inserção dos nossos alunos no mercado de trabalho”, observa a coordenadora do curso, Charlene Vicente Amâncio.
                            </p>
                            <p>
                                O EnModa 2015 prepara ainda mais atrações para quem for acompanhar os desfiles no Siso’s Hall. No evento, as criações de acadêmicos das segundas e quartas fases do curso também serão expostas. O projeto da segunda fase consiste na criação dos modelos para Barbie e cenários para estas bonecas, tendo como fonte de inspiração a indumentária no mundo. Com isso serão reproduzidas vestes ligadas a várias culturas. Já os alunos da quarta fase ficarão responsáveis por expor os adornos de cabeça com o tema “grandes acontecimentos mundiais”.
                            </p>
                            <p>
                                O EnModa é uma vitrine para apresentação dos projetos interdisciplinares do curso, que envolve vários segmentos de confecção como estamparias, tecidos, bordados e acessórios. Nas aulas do curso as estudantes finalizam os modelos que serão apresentados na próxima semana. Bordados, detalhes, recortes e finalizações seguem em ritmo acelerado para que o evento mantenha o alto nível das edições anteriores. 
